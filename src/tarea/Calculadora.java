    private void graficarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_graficarActionPerformed
        String opcion = funciones.getSelectedItem().toString();
        Plano p = new Plano(panel, 10);
        if (opcion.equals("Cuadratica")) {

            p.dibujar();
            Cuadratica.dibujar(p);
        } else if (opcion.equals("Cubica")) {

            p.dibujar();
            Cubica.dibujar(p);
        } else if (opcion.equals("Senoidal")) {

            p.dibujar();
            Senoidal.dibujar(p);

        } else if (opcion.equals("Cosenoidal")) {

            p.dibujar();
            Cosenoidal.dibujar(p);

        } else if (opcion.equals("Tangencial")) {

            p.dibujar();
            Tangencial.dibujar(p);

        } else if (opcion.equals("Logaritmica")) {

            p.dibujar();
            Logaritmica.dibujar(p);

        } else if (opcion.equals("Identidad")) {

            p.dibujar();
            Identidad.dibujar(p);

        } else if (opcion.equals("Constante")) {

            p.dibujar();
            Constante.dibujar(p);

        } else if (opcion.equals("Exponencial")) {

            p.dibujar();
            Exponencial.dibujar(p);

        }

    }//GEN-LAST:event_graficarActionPerformed

